// Чему будет равен x в примере ниже?

var a = 2;
var x = 1 + (a *= 2);

// Решение: В соответствии с порядком арифметических операций, в данной случае в матеметике и в языке они совпадают, выполняем то,
// что в скобках, а именно выражение "(a *= 2)" = "(a = a * 2)" = 2 * 2 = 4, и прибавляем к результату 1. Получаем x = 5.
// Ответ: x = 5.
